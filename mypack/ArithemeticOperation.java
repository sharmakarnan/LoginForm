package mypack;

public class ArithemeticOperation {

	public static void main(String[] args) {
		int resultA = -5 + 8 * 6;
		int resultB = (55+9) % 9;
		int resultC = 20 + -3*5 / 8;
		int resultD = 5 + 15 / 3 * 2 - 8 % 3;
		
		System.out.println("a. -5 + 8 * 6 = " + resultA);
		System.out.println("b. (55+9) % 9 = " + resultB);
		System.out.println("c. 20 + -3*5 / 8 = " + resultC);
		System.out.println("d. 5 + 15 / 3 * 2 - 8 % 3 = " + resultD);

	}

}
